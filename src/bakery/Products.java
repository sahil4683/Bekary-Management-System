    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed

        int i=cmb_cat.getSelectedIndex();
        int j=cmb_subcat.getSelectedIndex();
        int k=cmb_type.getSelectedIndex();
        int l=cmb_unit.getSelectedIndex();
        
        if (txt_pid.getText().equals("") || txt_pname.getText().equals("") || txt_rate.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please fill all fields properly!");
        }
        else if(j==0)
        {
            JOptionPane.showMessageDialog(this, "Please select proper sub category");        
        }
        else if(k==0)
        {
            JOptionPane.showMessageDialog(this, "Please select proper type");        
        }
        else if(l==0)
        {
            JOptionPane.showMessageDialog(this, "Please select proper unit of measurment");        
        }
        else {
            try {
                con = ConnectString.ConnectDb();
                PreparedStatement ps1 = con.prepareStatement("insert into Product values (?,?,?,?,?,?,?)");
                String a = txt_pid.getText();
                String b = a.trim();
                ps1.setString(1, b);
                ps1.setString(2, txt_pname.getText());
                ps1.setString(3, cmb_cat.getSelectedItem().toString());
                ps1.setString(4, cmb_subcat.getSelectedItem().toString());
                ps1.setString(5, cmb_type.getSelectedItem().toString());
                ps1.setString(6, cmb_unit.getSelectedItem().toString());
                ps1.setString(7, txt_rate.getText());
                ps1.execute();

                JOptionPane.showMessageDialog(this, "Record saved successfully!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);

            }
            update_tbl();
        }
   }//GEN-LAST:event_btn_saveActionPerformed

