    private void tbl_productMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_productMouseClicked
        try {
            con = ConnectString.ConnectDb();
            int row = tbl_product.getSelectedRow();
            String table_click = (tbl_product.getModel().getValueAt(row, 0).toString());

            String sql = "select * from Product where p_id='" + table_click + "' ";

            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();

            if (rs.next()) {
                String add1 = rs.getString("p_id");
                txt_pid.setText(add1);
                String add2 = rs.getString("name");
                txt_pname.setText(add2);
                String add3 = rs.getString("cat");
                cmb_cat.setSelectedItem(add3);
                String add4 = rs.getString("subcat");
                cmb_subcat.setSelectedItem(add4);
                String add5 = rs.getString("type");
                cmb_type.setSelectedItem(add5);
                String add6 = rs.getString("uom");
                cmb_unit.setSelectedItem(add6);
                String add7 = rs.getString("rate");
                txt_rate.setText(add7);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        btn_update.setEnabled(true);
        btn_delete.setEnabled(true);
        btn_save.setEnabled(false);
    }//GEN-LAST:event_tbl_productMouseClicked

